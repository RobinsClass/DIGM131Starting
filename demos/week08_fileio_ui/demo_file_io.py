import os
import json
import maya.cmds as cmds

# ============================================================
# PART 1: Basic File Writing and Reading
# ============================================================

# os.path.join builds paths that work on any OS.
# We'll write to the user's home directory for simplicity.
home = os.path.expanduser("~")
output_dir = os.path.join(home, "maya_exports")

# Create the directory if it doesn't exist yet
if not os.path.exists(output_dir):
    os.makedirs(output_dir)
    print(f"Created directory: {output_dir}")

# --- Writing a file ---
# 'with' is a CONTEXT MANAGER -- it automatically closes the file
# when the block ends, even if an error occurs.  Always use 'with'.
file_path = os.path.join(output_dir, "hello.txt")

with open(file_path, "w", encoding="utf-8") as f:  # "w" = write mode (overwrites)
    f.write("Hello from Maya!\n")
    f.write("This file was generated by a script.\n")

print(f"Wrote: {file_path}")

# --- Reading a file ---
with open(file_path, "r", encoding="utf-8") as f:  # "r" = read mode
    contents = f.read()
print("File contents:")
print(contents)

# --- Reading line by line ---
with open(file_path, "r", encoding="utf-8") as f:
    for line_number, line in enumerate(f, start=1):
        print(f"  Line {line_number}: {line.strip()}")


