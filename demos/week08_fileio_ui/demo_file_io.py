# ============================================================
# PART 3: JSON -- Saving Structured Data
# ============================================================
# JSON is a lightweight text format perfect for saving settings,
# scene layouts, tool configurations, etc.

# Let's create some objects in Maya and export their data.
cmds.polyCube(name="export_cube")
cmds.move(3, 1, -2, "export_cube")
cmds.polySphere(name="export_sphere")
cmds.move(-4, 0.5, 1, "export_sphere")

def gather_scene_data(object_names):
    """Collect transform data for a list of Maya objects."""
    scene_data = []
    for obj in object_names:
        if not cmds.objExists(obj):
            print(f"Warning: {obj} not found, skipping.")
            continue
        pos = cmds.xform(obj, q=True, worldSpace=True, translation=True)
        rot = cmds.xform(obj, q=True, worldSpace=True, rotation=True)
        scl = cmds.xform(obj, q=True, worldSpace=True, scale=True)
        obj_type = cmds.objectType(cmds.listRelatives(obj, shapes=True)[0])
        scene_data.append({
            "name": obj,
            "type": obj_type,
            "position": pos,
            "rotation": rot,
            "scale": scl,
        })
    return scene_data

objects_to_export = ["export_cube", "export_sphere"]
data = gather_scene_data(objects_to_export)

# --- Write JSON ---
json_path = os.path.join(output_dir, "scene_layout.json")

with open(json_path, "w", encoding="utf-8") as f:
    # indent=2 makes the file human-readable (pretty-printed)
    json.dump(data, f, indent=2)

print(f"\nExported scene data to: {json_path}")

# --- Read JSON back ---
with open(json_path, "r", encoding="utf-8") as f:
    loaded_data = json.load(f)

print("Loaded data:")
for item in loaded_data:
    print(f"  {item['name']}  type={item['type']}  pos={item['position']}")


# ============================================================
# PART 4: Reconstruct Scene from JSON
# ============================================================

def rebuild_from_json(json_file):
    """Delete existing objects and recreate them from saved JSON."""
    with open(json_file, "r", encoding="utf-8") as f:
        items = json.load(f)

    for item in items:
        # Remove old object if it exists
        if cmds.objExists(item["name"]):
            cmds.delete(item["name"])

        # Create the right primitive based on saved type
        if item["type"] == "mesh":
            # We stored the shape type; for a simple demo we check the name
            if "sphere" in item["name"].lower():
                cmds.polySphere(name=item["name"])
            else:
                cmds.polyCube(name=item["name"])

        # Apply saved transforms
        cmds.xform(item["name"], worldSpace=True, translation=item["position"])
        cmds.xform(item["name"], worldSpace=True, rotation=item["rotation"])
        cmds.xform(item["name"], worldSpace=True, scale=item["scale"])

    print(f"Rebuilt {len(items)} objects from {json_file}")

